# Liste pour stocker les DataFrames
all_data = []

def process_point(lat, lon, ds):
    # Sélection du point le plus proche
    ds_point = ds.sel(latitude=lat, longitude=lon, method="nearest")

    # Récupération des dates et conversion au format datetime
    time = pd.to_datetime(ds_point.valid_time.values)

    # Extraction et conversion température de Kelvin → Celsius
    t2m = ds_point["t2m"].values - 273.15

    # Création DataFrame
    df = pd.DataFrame({
        "time": time,
        "t2m_C": t2m,
    })

    # Ajout colonne année
    df["year"] = df["time"].dt.year

    # Moyenne annuelle
    df_yearly = df.groupby("year")["t2m_C"].mean().reset_index()

    # Ajout des colonnes lat/lon
    df_yearly["latitude"] = lat
    df_yearly["longitude"] = lon

    return df_yearly

